# calculator

a = int(input("add your firs number \n"))

b = int(input("add your second number \n"))

operator = input("add your adjective operator \n")

if operator=="+":

    result = a + b 

print(f"a + b ={result}")

if operator=="-":

    result = a - b

print(f"a - b = {result}") 

if operator=="*":

    result = a * b

print(f"a * b = {result}") 

if operator=="/":

    result = a / b

print(f"a / b = {result}") 

if operator=="**":

    result = a ** b

print(f"a ** b = {result}") 

if operator=="%":

    result = a % b

print(f"a % b = {result}") 



